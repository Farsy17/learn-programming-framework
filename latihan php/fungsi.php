<?php
		function tambah ( $a, $b){
			return $a + $b;
	}
		function kurang ( $a, $b){
			return $a - $b;
	}
		function bagi ($a, $b){
			return $a / $b;
	}
		function kali ( $a, $b){
			return $a * $b;
	}


	echo tambah (1,2)."<br>"; // isi nya pasti 3
	echo kurang (5,5)."<br>"; // isinya 0
	echo bagi (12,3)."<br>"; //isinya 4
	echo kali (2,5)."<br>"; //isinya 10